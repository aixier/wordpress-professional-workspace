/**
 * 生成响应式图片HTML
 */
function cardplanet_responsive_image($image_id, $sizes = 'cardplanet-showcase', $class = '', $alt = '') {
    if (!$image_id) {
        return '';
    }
    
    $image = wp_get_attachment_image($image_id, $sizes, false, array(
        'class' => $class,
        'alt' => $alt,
        'loading' => 'lazy'
    ));
    
    return $image;
}

/**
 * 获取案例研究链接
 */
function cardplanet_get_case_study_url($case_slug) {
    $page = get_page_by_path($case_slug);
    if ($page) {
        return get_permalink($page->ID);
    }
    return '#';
}

